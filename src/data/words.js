// ============================================================
// WordManager — 셔플 덱 방식 랜덤 단어 선택기
// ============================================================
class WordManager {
  constructor() {
    this.decks = {};
    this.indices = {};
    this.init();
  }

  init() {
    [1, 2, 3, 4, 5, 6].forEach(grade => {
      this.decks[grade] = this._shuffle(WORDS_BY_GRADE[grade].slice());
      this.indices[grade] = 0;
    });
  }

  getWord(gradeLevel) {
    const grade = Math.min(Math.max(gradeLevel, 1), 6);
    if (this.indices[grade] >= this.decks[grade].length) {
      const lastWord = this.decks[grade][this.decks[grade].length - 1];
      let newDeck = this._shuffle(WORDS_BY_GRADE[grade].slice());
      if (newDeck.length > 1 && newDeck[0].word === lastWord.word) {
        [newDeck[0], newDeck[1]] = [newDeck[1], newDeck[0]];
      }
      this.decks[grade] = newDeck;
      this.indices[grade] = 0;
    }
    return this.decks[grade][this.indices[grade]++];
  }

  getDistractors(correctWord, gradeLevel) {
    const grade = Math.min(Math.max(gradeLevel, 1), 6);
    const pool = WORDS_BY_GRADE[grade].filter(w => w.word !== correctWord.word);
    return this._shuffle(pool).slice(0, 3).map(w => w.word);
  }

  _shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
  }

  getTotalCount() {
    return Object.values(WORDS_BY_GRADE).reduce((sum, arr) => sum + arr.length, 0);
  }
}

window.wordManager = new WordManager();
